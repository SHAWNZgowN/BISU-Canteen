          public void retrieveReservation() {
        try {
            // SQL query with JOIN and WHERE to retrieve cart data based on user ID
            String query = "SELECT date, cust_name, name, quantity, price, total \n" +
                           "FROM reservation";

            // Prepare the statement
            PreparedStatement pst = con.prepareStatement(query);
            ResultSet rs = pst.executeQuery();

            // Clear existing data in the table model
            reseravtion_model = (DefaultTableModel) reservationTable.getModel();
            reseravtion_model.setRowCount(0);

            // Populate the model with retrieved data
            while (rs.next()) {
                String transacDate = rs.getString("date");
                String transacName = rs.getString("cust_name");
                String transacItem= rs.getString("name");
                int transacQuantity = rs.getInt("quantity");
                double transacPrice = rs.getDouble("price");
                double transacTotal = rs.getDouble("total");

                // Add the retrieved data to the table model
                reseravtion_model.addRow(new Object[]{transacDate, transacName, transacItem, transacQuantity, transacPrice, transacTotal});
            }

            // Set the model to the cart table
            reservationTable.setModel(reseravtion_model);

            // Close the statement and result set
            pst.close();
            rs.close();

        } catch (SQLException e) {
            // Handle any exceptions
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error retrieving cart data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
